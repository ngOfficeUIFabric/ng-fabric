'use strict';

/**
 * Enum for all possible button template types not public, only use within directive.
 * 
 * @readonly
 * @private
 * @enum {string}
 * 
 */
export enum ButtonTemplateType {
  actionButton,
  actionLink,
  primaryButton,
  primaryLink,
  commandButton,
  commandLink,
  compoundButton,
  compoundLink,
  heroButton,
  heroLink,
};
