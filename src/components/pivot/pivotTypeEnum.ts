'use strict';

/**
 *
 * Enum for pivot type supported by Office UI Fabric.
 *
 * @readonly
 * @enum {string}
 * @usage
 *
 * It is used to define type of the pivot that will rendered by `uif-pivot` directive.
 *
 * let type: string = PivotType[PivotType.tabs];
 */
export enum PivotType {
  regular,
  tabs
}
