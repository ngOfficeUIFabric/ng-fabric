'use strict';

/**
 *
 * Enum for pivot sizes supported by Office UI Fabric.
 *
 * @readonly
 * @enum {string}
 * @usage
 *
 * It is used to define size the pivot that will rendered by `uif-pivot` directive.
 *
 * let size: string = PivotSize[PivotSize.large];
 */
export enum PivotSize {
  'regular',
  'large'
}
