/**
 * Enum for supported sizes of the Persona component.
 * This enum is intended to be used as a striangular.
 *
 * @readonly
 * @enum {string}
 * @usage
 *
 *
 * let size: string = PersonaSize[PersonaSize.tiny];
 */
export enum PersonaSize {
  tiny,
  xsmall,
  small,
  medium,
  large,
  xlarge
};
